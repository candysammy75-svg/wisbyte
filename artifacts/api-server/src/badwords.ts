/** العبارات متعددة الكلمات — بتتمطابق كـ bigram أو trigram بعد التطبيع. */
const RAW_PHRASES: readonly string[] = [
  "يا شرموط",  "يا شرموطه",  "يا شرموطة",
  "يا عرص",    "يا عرصه",
  "يا خول",    "يا خوله",
  "يا متناك",  "يا متناكه",
  "يا منيوك",  "يا منيوكه",
  "يا قحبه",   "يا قحبة",
  "ابن الشرموطه", "ابن الشرموطة",
  "ابن المتناكه", "ابن المتناكة",
  "ابن القحبه",   "ابن القحبة",
];

// ══════════════════════════════════════════════════════════════════════════════
//  بناء Lookup سريع
// ══════════════════════════════════════════════════════════════════════════════

/** كلمات فردية: normalized → original */
const WORD_MAP = new Map<string, string>(
  RAW_BAD_WORDS.map((w) => [normalize(w), w])
);

/**
 * عبارات متعددة: normalized (كل كلمة متطبّعة ومفصولة بـ space واحد) → original.
 * بيتحوّل لـ bigrams/trigrams بناءً على عدد الكلمات.
 */
const PHRASE_MAP = new Map<string, string>(
  RAW_PHRASES.map((phrase) => {
    const normalizedPhrase = phrase
      .split(/\s+/)
      .map(normalize)
      .filter((t) => t.length > 0)
      .join(" ");
    return [normalizedPhrase, phrase];
  })
);

// ══════════════════════════════════════════════════════════════════════════════
//  Tokenizer
// ══════════════════════════════════════════════════════════════════════════════

/** بيقسّم النص لـ tokens نظيفة مطبّعة. */
function tokenize(text: string): string[] {
  return text
    .split(/[\s\u0021-\u002F\u003A-\u0040\u005B-\u0060\u007B-\u007E\u060C\u061B\u061F\u06D4،؟!؛…\-_|]+/)
    .map(normalize)
    .filter((t) => t.length >= 2);
}

/**
 * بيولّد n-grams من قائمة tokens.
 * @param tokens قائمة الـ tokens المطبّعة
 * @param n حجم الـ gram (2 = bigrams, 3 = trigrams)
 * @returns قائمة strings كل منها tokens مدمجة بمسافة
 */
function ngrams(tokens: string[], n: number): string[] {
  const result: string[] = [];
  for (let i = 0; i <= tokens.length - n; i++) {
    result.push(tokens.slice(i, i + n).join(" "));
  }
  return result;
}

// ══════════════════════════════════════════════════════════════════════════════
//  الدالة الرئيسية
// ══════════════════════════════════════════════════════════════════════════════

/**
 * بيبحث عن أول شتيمة في الرسالة — كلمة كاملة أو عبارة كاملة.
 *
 * الكلمة الفردية لازم تكون token منفصل (مش جزء من كلمة أطول).
 * العبارة المتعددة بتتمطابق كـ bigram/trigram متتالي من الـ tokens.
 *
 * @returns الكلمة/العبارة الأصلية لو اتلقيت، وإلا `undefined`.
 */
export function findBadWord(text: string): string | undefined {
  const tokens = tokenize(text);

  // 1. بحث في الكلمات الفردية
  for (const token of tokens) {
    const original = WORD_MAP.get(token);
    if (original !== undefined) return original;
  }

  // 2. بحث في العبارات (bigrams + trigrams)
  if (tokens.length >= 2) {
    for (const bigram of ngrams(tokens, 2)) {
      const original = PHRASE_MAP.get(bigram);
      if (original !== undefined) return original;
    }
  }
  if (tokens.length >= 3) {
    for (const trigram of ngrams(tokens, 3)) {
      const original = PHRASE_MAP.get(trigram);
      if (original !== undefined) return original;
    }
  }

  return undefined;
}
