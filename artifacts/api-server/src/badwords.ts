// ══════════════════════════════════════════════════════════════════════════════
//  التطبيع (Normalization)
// ══════════════════════════════════════════════════════════════════════════════

/** بيشيل التشكيل والحروف الغير مرئية وبيوحّد الحروف المتشابهة. */
function normalize(word: string): string {
  return word
    // 1. شيل الأحرف الغير مرئية وصفر العرض (Zero-width)
    .replace(/[\u200B-\u200F\u2060-\u2064\uFEFF\u00AD]/g, "")
    // 2. شيل التشكيل الكامل (tashkeel, shadda, sukun, etc.)
    .replace(/[\u0610-\u061A\u064B-\u065F\u0670\u06D6-\u06DC\u06DF-\u06E4\u06E7\u06E8\u06EA-\u06ED]/g, "")
    // 3. توحيد الألف بكل أشكالها (أ إ آ ٱ)
    .replace(/[أإآٱ]/g, "ا")
    // 4. توحيد حاملات الهمزة الأخرى: ؤ → و ، ئ → ي ، ء → ا
    .replace(/ؤ/g, "و")
    .replace(/ئ/g, "ي")
    .replace(/ء/g, "ا")
    // 5. توحيد الألف المقصورة مع الياء
    .replace(/ى/g, "ي")
    // 6. توحيد التاء المربوطة مع الهاء
    .replace(/ة/g, "ه")
    // 7. شيل التطويل
    .replace(/ـ/g, "")
    // 8. شيل المسافات الزائدة
    .trim();
}

// ══════════════════════════════════════════════════════════════════════════════
